#include "../../so_long.h"

void	ft_putnbr(int n, int *cp)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		*cp = *cp + 11;
		return ;
	}
	else if (n < 0)
	{
		ft_putchar('-', cp);
		n *= -1;
	}
	if (n >= 0 && n < 10)
		ft_putchar(n + '0', cp);
	else
	{
		ft_putnbr(n / 10, cp);
		ft_putchar((n % 10) + '0', cp);
	}
}
